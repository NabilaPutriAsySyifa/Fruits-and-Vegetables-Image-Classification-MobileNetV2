# Augmentasi data gambar dengan image data generator (untuk dataset Buah & Sayur)
train_datagen = ImageDataGenerator(
    rescale=1./255,               # Normalisasi piksel dari [0, 255] ke [0, 1]
    rotation_range=40,            # Rentang rotasi acak (derajat) Buah bisa miring ke segala arah
    width_shift_range=0.2,        # Geser horizontal acak
    height_shift_range=0.2,       # Geser vertikal acak
    shear_range=0.2,              # Perubahan bentuk (shear) acak
    zoom_range=0.2,               # Zoom acak
    brightness_range=[0.8, 1.2],  # Kecerahan pada gambar
    horizontal_flip=True,         # Balik horizontal acak
    # vertical_flip=True,         # Untuk dibalik atas-bawah
    fill_mode='nearest'           # Pengisian piksel yang hilang setelah transformasi
)

val_datagen = ImageDataGenerator(rescale=1./255)

# Load dataset gambar Fruits & Vegetables dari direktori untuk augmentasi data
