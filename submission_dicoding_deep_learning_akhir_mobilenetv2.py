"""## Modeling"""

# Variabel global untuk parameter arsitektur model
input_shape = (224, 224, 3)

# Dapatkan jumlah kelas
num_classes = len(train_generator.class_indices)

# Load Base Model MobileNetV2
base_model = MobileNetV2(
    weights='imagenet',
    include_top=False,
    input_shape=input_shape
)

# Proses Fine-Tuning
base_model.trainable = True
fine_tune_at = 100
for layer in base_model.layers[:fine_tune_at]:
    layer.trainable = False

# Arsitektur model sequential
model = Sequential()
model.add(base_model)
model.add(Conv2D(64, (3, 3), activation='relu', padding='same'))
model.add(MaxPooling2D((2, 2)))

model.add(GlobalAveragePooling2D())
model.add(Dense(256, activation='relu'))
model.add(Dropout(0.5))
model.add(Dense(num_classes, activation='softmax'))

print("Arsitektur Fine-Tuned MobileNetV2 dengan Conv2D berhasil dibangun!")

# Kompilasi model MobileNetV2 yang termasuk model CNN Deep Learning
model.compile(optimizer=Adam(learning_rate=0.00001),
              loss='categorical_crossentropy',
              metrics=['accuracy'])

# Menampilkan ringkasan arsitektur model MobileNetV2
model.summary()

# Dapatkan label dari generator Fruits & Vegetables
class_labels = train_generator.classes

# Hitung bobot untuk mengatasi sedikit ketidakseimbangan data
class_weights = compute_class_weight(
    class_weight='balanced',
    classes=np.unique(class_labels),
    y=class_labels
)

# Ubah menjadi format dictionary agar bisa dibaca oleh model.fit
class_weights_dict = dict(enumerate(class_weights))

print("\nClass Weights yang akan digunakan untuk 20 kelas:")
print(class_weights_dict)

# Latih model CNN Deep Learning
EPOCHS = 50

print('\nMemulai pelatihan model MobileNetV2...')

# Custom Callback untuk stop training jika nilai akurasi sudah optimal (≥ 95%)
class TargetAccuracy(tf.keras.callbacks.Callback):
    def on_epoch_end(self, epoch, logs={}):
        if(logs.get('accuracy') >= 0.95 and logs.get('val_accuracy') >= 0.95):
            print("\nTarget akurasi 95% pada Train dan Val tercapai! Menghentikan training...")
            self.model.stop_training = True

target_callback = TargetAccuracy()

# Callback standar: Berhenti jika loss validasi tidak membaik selama 5 epoch
early_stop = EarlyStopping(monitor='val_loss',
                           patience=5,
                           restore_best_weights=True)

# Simpan model
model_checkpoint = ModelCheckpoint('mobilenetv2_fruits_veg_best.keras',
                                   monitor='val_accuracy',
                                   save_best_only=True,
                                   mode='max')

# Optimalisasi learning rate agar titik optimal bobot tercapai
reduce_lr = ReduceLROnPlateau(monitor='val_loss',
                              factor=0.2,
                              patience=3,
                              min_lr=0.00001) # Batas minimum

# Pelatihan model
history = model.fit(
    train_generator,
    steps_per_epoch=train_generator.samples // BATCH_SIZE,
    epochs=EPOCHS,
    validation_data=val_generator,
    validation_steps=val_generator.samples // BATCH_SIZE,
    callbacks=[early_stop, model_checkpoint, target_callback, reduce_lr],
    class_weight=class_weights_dict
)

"""## Evaluasi dan Visualisasi"""

# Evaluasi model MobileNetV2 dengan data uji (test) Fruits & Vegetables
test_loss, test_acc = model.evaluate(test_generator)
print(f"Test accuracy MobileNetV2: {test_acc:.4f}, Test loss: {test_loss:.4f}")

# Plot hasil train model MobileNetV2 dari history
def plot_history(history):
    acc = history.history['accuracy']
    val_acc = history.history['val_accuracy']
    loss = history.history['loss']
    val_loss = history.history['val_loss']

    epochs_range = range(len(acc))

    # Plot hasil akurasi Training dan Validation
    plt.figure(figsize=(12, 5))
    plt.subplot(1, 2, 1)
    plt.plot(epochs_range, acc, label='Training Accuracy')
    plt.plot(epochs_range, val_acc, label='Validation Accuracy')
    plt.legend(loc='lower right')
    plt.title('Training & Validation Accuracy (MobileNetV2)')

    # Plot hasil loss Training and Validation
    plt.subplot(1, 2, 2)
    plt.plot(epochs_range, loss, label='Training Loss')
    plt.plot(epochs_range, val_loss, label='Validation Loss')
    plt.legend(loc='upper right')
    plt.title('Training & Validation Loss (MobileNetV2)')

    plt.tight_layout()
    plt.show()

# Terapkan fungsi plot untuk melihat grafik hasil training MobileNetV2
plot_history(history)

