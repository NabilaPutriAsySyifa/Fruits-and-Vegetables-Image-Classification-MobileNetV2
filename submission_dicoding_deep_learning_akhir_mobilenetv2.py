# Fungsi untuk mengecek jumlah gambar dan ukuran resolusi
def print_images_resolution(directory):
    unique_sizes = set()
    total_images = 0

    # Tambahan sorted() agar nama kelas berurutan sesuai abjad
    for subdir in sorted(os.listdir(directory)):
        subdir_path = os.path.join(directory, subdir)

        # Pengamanan: Lewati jika item tersebut bukan sebuah folder
        if not os.path.isdir(subdir_path):
            continue

        image_files = os.listdir(subdir_path)
        num_images = len(image_files)
        print(f"Jumlah gambar pada kelas '{subdir}': {num_images}")
        total_images += num_images

        # Hanya 5 gambar yang dilihat resolusinya
        for i, img_file in enumerate(image_files):
            if i >= 5:
                break
            img_path = os.path.join(subdir_path, img_file)

            # Gunakan try-except untuk berjaga-jaga jika ada file gambar korup dari dataset Kaggle
            try:
                with Image.open(img_path) as img:
                    unique_sizes.add(img.size)
            except Exception:
                continue

        print(f"Resolusi pada gambar sampel sebanyak 5 data:")
        for size in unique_sizes:
            print(f"- {size}")
        print("------------------------------")
        unique_sizes.clear()

    print(f"\nTotal gambar pada seluruh kelas dalam direktori ini: {total_images}")

